package ru.mdm.documents.model;

/**
 * Тип атрибута документа
 */
public enum PropertyType {

    /**
     * Текст
     */
    TEXT,

    /**
     * Целое число
     */
    INTEGER,

    /**
     * Вещественное число
     */
    DOUBLE,

    /**
     * Признак
     */
    BOOLEAN,

    /**
     * Дата и время
     */
    DATETIME
}
